##5- Faça um algorítmo que converta metros para centímetros

#Insira o tamanho em metros
#Multiplicar o valor por 100
#Mostrar o resultado

metros=input('Digite o tamanho em metros: ')
cm=int(metros)*100
print(metros,'metros é igual a',cm,'centímetros')
print('{0} metros é igual a {1} centímetros'.format(metros,cm))

